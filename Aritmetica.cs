using System;

namespace Calculos
{  // Implementa operações aritméticas 
    internal class Aritmetica
    {
        public int Somar (int x, int y)
        {
            return x + y;
        }
        public int Subtrair(int x, int y)
        {
            return x - y;
        }
        public int Multiplicar (int x, int y)
        {
            return x * y;
        }


    }
}
